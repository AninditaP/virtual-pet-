
var dog_image,hd_image;
var dog;
var database,FoodStock,food;
var Food;

function preload()
{
  dog_image=loadImage("dogImg.png");
  happy_img=loadImage("dogImg1.png")
}

function setup() {
  createCanvas(700, 700);
  database=firebase.database();

  dog=createSprite(350,500,30,30);
  dog.addImage(dog_image);
  dog.scale=0.3;
  FoodStock=database.ref('food');
  FoodStock.on("value",read);

}

function draw() {  

  background(46,139,87);
  textSize(30);
  fill("white");
  text("Food Remaining:"+Food,200,300);
  text("Press the 'UP' arrow to feed DRAGO milk ",70,100);

  text(food,100,100);
  if(keyWentDown(UP_ARROW) && Food!==undefined){  
    write();
    dog.addImage(happy_img);
    console.log(Food);
  }

  drawSprites();
}

function read(data){
   Food=data.val();
}

function write(){
    if(Food<=0){
      Food=0;
    }
    
   database.ref('/').set({
      'food':Food-1,
   })
}





































































































































































































































































































































































































































































































































































































































































































































